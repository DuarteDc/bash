#Muestra los procesos asociados a una terminal
ps
#Muestra todos los procesos
ps x

#Muestra información mas detallada de los procesos
ps aux | less

#Top muestra los procesos en ejecución de forma dinamica
top
# S Para cambiar el intervalo de actualización
# U Para filtrar por usuario
# E Para cambiar el valor de consumo del proceso
# O Filtrar por columna de top